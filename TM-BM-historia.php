                 <h3>Reseña historica</h3>
                 <p>El Ballet Clásico y Moderno Municipal sale de los parámetros convencionales de compañías de danzas. No se puede catalogar a la compañía como clásica o moderna pues su repertorio y sus bailarines interpretan perfectamente un &quot;Lago de los Cisnes&quot; o &quot;Don Quijote&quot; hasta las piezas más audaces de coreógrafos contemporáneos como Oscar Araiz, Hilda Riveros y Ana María Stekelman. También otra característica de la compañía es interpretar los clásicos con un espíritu innovador y en los últimos años ha sumado a su repertorio obras como &quot;Madame Butterfly&quot;, &quot;Los Tres Mosqueteros&quot;, &quot;La Viuda Alegre&quot;, &quot;Pas de deux Paraguayo&quot;, &quot;Despertar Campesino&quot;, &quot;Madame Lynch&quot;, &quot;Francesco&quot; que fueron estrenos mundiales en Paraguay.</p>
                 <p>El Ballet Clásico Municipal fue creado en la Ciudad de Asunción en 1972. Tala Ern de Retivoff fue su fundadora y su primera Directora. Pocos años más tarde Teresa Capurro fusiona con el Ballet Moderno Municipal creado en 1971 por María Retivoff, adoptando el nombre de Ballet Clásico y Moderno Municipal.</p>
                 <p>Posteriormente tienen a su cargo la Dirección de la Compañía: Teresa Capurro, Nidia Neumayer, Elizabeth Laurent, Alcy Acuña, Emilio Barrientos, Miguel Florenciáñez, Nicole Dijkhuis, Rubén Franco. En 1990, Miguel Bonnín asume la dirección implementando una reestructuración de la Compañía aumentando los horarios de trabajo para elevar el nivel técnico y artístico. Desde este año también realiza audiciones periódicas para que los bailarines puedan acceder al ballet. </p>
                 <p>En 1992 se crea la Asociación de Amigos del Ballet Municipal desempeñado un papel fundamental de apoyo al desarrollo del Ballet. </p>
                 <p>En estos 40 años la Compañía Municipal de la Ciudad de Asunción ha realizado grandes espectáculos, funciones de gala, de beneficencia, y difusión gratuita.La Compañía se presenta en Asunción y en ciudades del interior del Paraguay asi como también en el extranjero en países como Brasil, Argentina, Uruguay, Panamá y Cuba. Cuenta con repertorio variado con más de cien obras presentadas, estrenadas e interpretadas tanto por grandes figuras nacionales e internacionales como Ana Botafogo, Cecilia Kerche, Nydia Neumayer, Karina Olmedo, Juan Pablo Ledo, Miguel Ángel Serrano, Alejandro Parente, Natalie Quernet y Olivie Patey, entre otros.</p>
                   <p>También ha contado con creadores y maestros nacionales e internacionales como Mario Galizzi, Jaime Pinto, Osvaldo Beiro, Francoise Adret, Rafael Saladrigas, Doris Petroni, Misha Bart, Dulce Díaz Somonte, Oliver Patey, Vitalij Iacovlev, Lupe Calzadilla, Francisco Carvallo, Robson Maia, Carmiña Martínez, Mary Carmen Niella, Marilyn Candia, entre otros.</p>
                 <p>Actualmente el Ballet Clásico y Moderno Municipal es el elenco oficial del Teatro Municipal de la ciudad de Asunción.</p>
